public class Wrap {
    public static void main(String[] args) {
        int x= Integer.parseInt("123");
        System.out.println(x);

        Integer x1= Integer.valueOf("10110", 2);
        int y= x1.intValue();
        System.out.println(y);

        Double d1= Double.valueOf("57");
        Double z= d1.doubleValue();
        System.out.println(z);

    }
}


// we use wrapper class for converting data from primitive to non-primitive and non-primitive to primitive.
